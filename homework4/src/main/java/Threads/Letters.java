package Threads;

public class Letters {

    final char symb1;
    final char symb2;
    final char symb3;

    public Letters(char symb1, char symb2, char symb3) {
        this.symb1 = symb1;
        this.symb2 = symb2;
        this.symb3 = symb3;
    }

    public char getSymb1() {
        return symb1;
    }

    public char getSymb2() {
        return symb2;
    }

    public char getSymb3() {
        return symb3;
    }
}
